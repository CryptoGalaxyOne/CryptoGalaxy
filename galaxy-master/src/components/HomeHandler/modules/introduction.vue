        <h2>2、成为星球主后如何获得更多游戏资产?</h2>
        <p>(1)挖掘税——星球主收取在星球上挖矿的探索者收益中的15%作为税收。(挖掘税率会受到星球的等 级，挖掘人数等各方面因素影响而调整。)星球主可以在“发现”界面发布广告吸引更多探索者来挖矿。</p>
        <p>(2)星球升值:1星球等级越高，产量越大2星球等级越高，出售价值越大3星球等级越高，产出原料品 质越高</p>
        <h2>3、如何使用矿机</h2>
        <p>目前游戏内有三款矿机，分别挖取白矿、绿矿、蓝矿，不同的机器人只能对应自己可挖掘的原料。</p>
        <p>其中白矿机器人可以免费外，其他机器人可以从官方或是其他玩家手里购买。每个机器人的原料携带量有 限，如果满了则不会继续挖掘，需要玩家手动提取。玩家可以通过消耗游戏积分来进行升级，升级后挖掘速 度和原料携带量都会上升。除了免费机器人外，其他机器人都可以交易和售卖。每次挖掘前需要提取一定的 原力值来进行机器人燃料补充，每6 个原力值可以激活一次机器人。</p>
        <p>(原力值会每隔一小段时间自动产生，点击收取即可)</p>
        <h2>4 、如何获取原力?</h2>
        <p>(1)手动抓取:打开游戏，30-60秒内 星球上会出现白光原力，点击星球上的白光原力，原力会被抓取到 原力槽，大概 1-3 分钟左右会出现一次原力， 直至原力槽 6 格收取满，每格原力可供矿机挖矿1小时。</p>
        <p>(2)还可购买原力槽，每个原力槽有 6 格原力，原力处最多可放置 3 个原力槽即 24 格原力;</p>
        <p>(3)商城内也可购买周/月/季度/年度原力石，持续给矿机提供不同时常的原力能量。</p>
        <h2>5、如何挖矿?</h2>
        <p>(1)从“星球”⻚面进入，在有原力的情况下，参照流程:</p>
        <p>【点击发现】---【选择任意星球】---【登陆开采】---【跳入矿机⻚面】---【开始挖矿】---【确定开 始挖矿】---【矿机挖矿中】;</p>
        <p>(2)从“矿机”⻚面进入，在有原力的情况下，参照流程:</p>
        <p>【点击矿机】---【开始挖矿】---【提示选择星球】---【跳入星球⻚面】---【选择任意星球】---【登 陆开采】---【跳入矿机⻚面】---【开始挖矿】---【确定开始挖矿】---【矿机挖矿中】;</p>
        <p>(3)更换星球挖矿，参照流程:</p>
        <p>【点击矿机】---【停止挖矿】---【矿机成功停止挖矿】---【等待矿机冷却完成】---【开始挖矿】--- 【重复挖矿路径】;</p>
        <p>*在挖矿开始前， 确保已获取原力;</p>
        <p>*更换星球挖矿时， 矿机有60s冷却时间;</p>
        <p>*在别人的星球上挖矿，星球主人收取 15% 的矿产收益作为税收。</p>
        <p>*最多可以同时开启 9 台不同类矿机的 GalaBot 机器人采矿操作。</p>
