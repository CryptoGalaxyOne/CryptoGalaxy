        <h2>3.创作背景</h2>
        <p>《CryptoGalaxy》是Zeepin Chain上的第一款区块链娱乐应用。近两年区块链热潮席卷了各行各业，成为 当下最热⻔也是最受瞩目的信息技术之一，相比较与大数据、云计算、人工智能等这些信息技术，区块链 的“去中心化”、“不可篡改”、“公开透明”等特性成为了人们交流的热点。而在金融领域提起区块链，大家的 第一反应就是数字货币。这样的反应源于区块链技术是随着比特币的诞生而出现。</p>
        <p>但其实除了数字货币，区块链技术在知识产权、游戏、支付清算、证券交易、供应链金融、保险、征信等领 域都能发挥很大的作用。</p>
        <p>当前，区块链技术的发展尚处于早期，伴随着真正的场景落地，将会展现出其内在的巨大价值和潜力，游戏 作为新技术的最佳实践载体，为区块链与文娱产业的融合打开了一扇大⻔。传统游戏本身就以中心化的方式 实现了虚拟内容归属权益、虚拟商品交易、游戏积分结算等功能，这与区块链技术的特性不谋而合，而区块 链的运行原理又拥有内容自发性、数据不可篡改特性的先天优势。</p>
        <p>区块链以非对称加密的方式保证了交易的准确性、安全性和可溯性。非对称加密除了保证信息的安全性之 外，还被用于《Crypto Galaxy》账户体系的身份验证，确保玩家身份的准确性，我们希望借助于区块链技 术中的透明、公开、公正等特性，解决现有游戏中玩家对运营商的不信任问题，让玩家在游戏娱乐中体会到 区块链账本、智能合约相关的功能机制。</p>

